	/**
	 * Metodo que filtra la array unidimensional a una matriz de tiles segun el tipo.
	 * 
	 * @return Tile[][] result
	 */
	public static Tile[][] filtrarTiles() {
		Tile[][] result = new Tile[80][46];

		int x = 0;
		int y = 0;

		for (int index = 0; index < map.length; index++) {
			if (x % result.length == 0) {
				y++;
				x = 0;
			}
			if (y % result[0].length == 0) {
				y = 0;
			}

			result[x][y] = new Tile(Tipo.parseTipo(map[index]));
			x++;
		}

		return result;
	}



